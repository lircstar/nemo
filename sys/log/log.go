func (log *Logger) Recover() {
	if r := recover(); r != nil {
		buf := make([]byte, 4096)
		l := runtime.Stack(buf, false)
		log.Error("%v: %s", r, buf[:l])
	}
}

func (log *Logger) LogFile(filepath string) bool {
	now := time.Now()
	filename := fmt.Sprintf("%s[%d%02d%02d%02d%02d%02d].log",
		log.name,
		now.Year(),
		now.Month(),
		now.Day(),
		now.Hour(),
		now.Minute(),
		now.Second())

	filename = path.Join(filepath, filename)
	mode := os.O_RDWR | os.O_CREATE | os.O_APPEND
	file, err := os.OpenFile(filename, mode, 0666)
	if err != nil {
		return false
	}
	log.enableColor = false
	log.fileOutput = file

	return true
}
func str2loglevel(level string) Level {

	switch level {
	case "debug":
		return Level_Debug
	case "info":
		return Level_Info
	case "warn":
		return Level_Warn
	case "error":
		return Level_Error
	case "fatal":
		return Level_Fatal
	}

	return Level_Debug
}

func (log *Logger) SetLevelByString(level string) {
	log.SetLevel(str2loglevel(level))
}

func (log *Logger) SetLevel(lv Level) {
	log.level = lv
}

func (log *Logger) Level() Level {
	return log.level
}

func (log *Logger) SetPanicLevelByString(level string) {
	log.panicLevel = str2loglevel(level)
}

func str2logflag(f string) int {
	flag := LstdFlags
	flags := strings.Split(f, "|")
	for _, s := range flags {
		v := strings.ToLower(strings.Trim(s, " "))
		switch v {
		case "longfile":
			flag |= Llongfile
			break
		case "shortfile":
			flag |= Lshortfile
			break
		case "longtime":
			flag |= Lmicroseconds
			break
		}
	}
	return flag
}

func (log *Logger) SetFlags(flag int) {
	log.flag = flag
}

func (log *Logger) SetFlagsByString(f string) {
	log.flag = str2logflag(f)
}

// SetColorFile 注意, 加色只能在Goland的main方式启用, Test方式无法加色
func (log *Logger) SetColorFile(file *ColorFile) {
	log.colorFile = file
	log.colorFile.Load("color_sample.json")
	log.enableColor = true
}

func (log *Logger) IsDebugEnabled() bool {
	return log.level == Level_Debug
}

func (log *Logger) defaultColorFile() {
	log.colorFile = NewColorFile()
	log.colorFile.Load("color_sample.json")
	if log.fileOutput == nil {
		log.enableColor = true
	}
}

func (log *Logger) Close() {
	if log.fileOutput != nil {
		log.fileOutput.Close()
	}

	log.fileOutput = nil
}

